package com.syntax.class07;

public class IncrementDecrement {

	public static void main(String[] args) {
		//can only do plus or minus 1, no other numbers or operators
		int x=10;
		x=x+1;
		x+=1;	//different way of adding 1
		// another way to add +1 to a variable
		x++;
		System.out.println(x);
		
		int y=90;
		y-=1; //shorter way of y=y-1; another way to subtract 1 to a variable 
		y--;
		System.out.println(y);
		
		//1++; not allowed 
		//2--; not allowed

	}

}
